    private void llenarPizza() {

        modeloProductos mp = new modeloProductos();
        ArrayList<productos> listado = mp.combo_p();
        cbx_pizza.removeAllItems();

        for (int i = 0; i < listado.size(); i++) {
            cbx_pizza.addItem(new productos(listado.get(i).getId_producto(), listado.get(i).getProducto(), listado.get(i).getTamano(), listado.get(i).getPrecio()));
        }

    }

    private void llenarBebida() throws SQLException {

        modeloProductos mb = new modeloProductos();
        ArrayList<productos> listadob = mb.combo_b();
        cbx_bebida.removeAllItems();

        for (int i = 0; i < listadob.size(); i++) {
            cbx_bebida.addItem(new productos(listadob.get(i).getId_producto(), listadob.get(i).getProducto(), listadob.get(i).getTamano(), listadob.get(i).getPrecio()));
        }

    }
    
    private void totalPagar(){
        
        totalPagar=0.0;
        
        for(int i=0; i<tabla_pro.getRowCount();i++){
            
            String sub = String.valueOf(tabla_pro.getModel().getValueAt(i, 4));
            double precio_i = Double.parseDouble(sub);
            totalPagar += precio_i;
            
        }
        
        lbl_total.setText(String.valueOf(totalPagar));  
    }
    
